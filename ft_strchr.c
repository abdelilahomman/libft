char	*ft_strchr(const char *s, int c)
{
	char	*str;
	int		i;

	i = 0;
	str = (char *)s;
	while (str[i] != '\0' && c != str[i])
		i++;
	if (str[i] == c)
		return (str + i);
	return (0);
}
